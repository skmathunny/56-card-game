#!/usr/bin/env node
/**
 * Generates placeholder PNG card images and TypeScript image-map files for each deck.
 *
 * Usage:
 *   node scripts/generate-card-assets.js
 *
 * To add a new deck without code changes:
 *   1. Drop real PNG images into  assets/cards/<deckId>/<rank>_<suit>.png  (and back.png)
 *   2. Add an entry to  src/decks/deckConfig.json
 *   3. Re-run this script to regenerate the TypeScript image map for the new deck
 *      (or hand-write src/decks/images/<deckId>.ts following the same pattern)
 *
 * Card image naming: {rank}_{suit}.png  — matches card IDs used by the engine.
 * Suits: spades, hearts, diamonds, clubs
 * Ranks: J, 9, A, 10, K, Q
 */

const { deflateSync } = require('zlib');
const fs   = require('fs');
const path = require('path');

const RANKS = ['J', '9', 'A', '10', 'K', 'Q', '7', '8'];
const SUITS = ['spades', 'hearts', 'diamonds', 'clubs'];

const CARD_W = 80;
const CARD_H = 112;

// ── CRC32 for PNG chunks ────────────────────────────────────────────────────

const CRC_TABLE = (() => {
  const table = new Uint32Array(256);
  for (let n = 0; n < 256; n++) {
    let c = n;
    for (let k = 0; k < 8; k++) c = (c & 1) ? 0xedb88320 ^ (c >>> 1) : c >>> 1;
    table[n] = c;
  }
  return table;
})();

function crc32(buf) {
  let crc = 0xffffffff;
  for (let i = 0; i < buf.length; i++) crc = CRC_TABLE[(crc ^ buf[i]) & 0xff] ^ (crc >>> 8);
  return (crc ^ 0xffffffff) >>> 0;
}

function chunk(type, data) {
  const len   = Buffer.alloc(4);
  len.writeUInt32BE(data.length);
  const typeB = Buffer.from(type, 'ascii');
  const crcB  = Buffer.alloc(4);
  crcB.writeUInt32BE(crc32(Buffer.concat([typeB, data])));
  return Buffer.concat([len, typeB, data, crcB]);
}

/**
 * Creates a minimal valid PNG filled with a solid RGB color.
 * Replace with real card art — the image-map files use static require() paths
 * so only the PNG content needs to change, not the code.
 */
function makeSolidPng(w, h, [r, g, b]) {
  const ihdr = Buffer.alloc(13);
  ihdr.writeUInt32BE(w, 0);
  ihdr.writeUInt32BE(h, 4);
  ihdr[8] = 8; // 8-bit
  ihdr[9] = 2; // color type: truecolor RGB

  // One filter byte (0 = None) + RGB triplet per pixel, repeated for h rows
  const raw = Buffer.alloc(h * (1 + w * 3));
  for (let y = 0; y < h; y++) {
    raw[y * (1 + w * 3)] = 0;
    for (let x = 0; x < w; x++) {
      const i = y * (1 + w * 3) + 1 + x * 3;
      raw[i] = r; raw[i + 1] = g; raw[i + 2] = b;
    }
  }

  const PNG_SIG = Buffer.from([137, 80, 78, 71, 13, 10, 26, 10]);
  return Buffer.concat([
    PNG_SIG,
    chunk('IHDR', ihdr),
    chunk('IDAT', deflateSync(raw)),
    chunk('IEND', Buffer.alloc(0)),
  ]);
}

// ── Deck color palettes for placeholders ───────────────────────────────────
// Values are [R, G, B].  Replace PNG files with real art; colors are irrelevant after that.

const DECK_PALETTES = {
  classic: {
    back:    [26,  107, 181],
    spades:  [255, 255, 255],
    clubs:   [255, 255, 255],
    hearts:  [255, 235, 235],
    diamonds:[255, 235, 235],
  },
  royal: {
    back:    [20,  20,  50],
    spades:  [28,  28,  60],
    clubs:   [28,  28,  60],
    hearts:  [60,  10,  30],
    diamonds:[60,  10,  30],
  },
  minimal: {
    back:    [90,  90,  120],
    spades:  [244, 244, 250],
    clubs:   [244, 244, 250],
    hearts:  [250, 244, 244],
    diamonds:[250, 244, 244],
  },
  orthodox: {
    back:    [20,  80,  20],
    spades:  [255, 255, 255],
    clubs:   [255, 255, 255],
    hearts:  [255, 235, 235],
    diamonds:[255, 235, 235],
  },
};

// ── Paths ───────────────────────────────────────────────────────────────────

const APP_ROOT   = path.join(__dirname, '..');
const ASSETS_DIR = path.join(APP_ROOT, 'assets', 'cards');
const IMAGES_DIR = path.join(APP_ROOT, 'src', 'decks', 'images');

fs.mkdirSync(IMAGES_DIR, { recursive: true });

// ── Generate per-deck assets ────────────────────────────────────────────────

for (const [deckId, palette] of Object.entries(DECK_PALETTES)) {
  const deckDir = path.join(ASSETS_DIR, deckId);
  fs.mkdirSync(deckDir, { recursive: true });

  // Card faces
  for (const suit of SUITS) {
    for (const rank of RANKS) {
      const file = path.join(deckDir, `${rank}_${suit}.png`);
      // Only write if missing — preserves real art that has been dropped in
      if (!fs.existsSync(file)) {
        fs.writeFileSync(file, makeSolidPng(CARD_W, CARD_H, palette[suit]));
      }
    }
  }

  // Card back
  const backFile = path.join(deckDir, 'back.png');
  if (!fs.existsSync(backFile)) {
    fs.writeFileSync(backFile, makeSolidPng(CARD_W, CARD_H, palette.back));
  }

  console.log(`✓ assets/cards/${deckId}/  (${RANKS.length * SUITS.length + 1} files)`);

  // TypeScript image map — static require() paths satisfy Metro bundler
  const lines = [
    `// Auto-generated by scripts/generate-card-assets.js — do not edit manually.`,
    `// To add real card art: replace PNG files in assets/cards/${deckId}/ and re-run the script.`,
    `const images: Record<string, any> = {`,
  ];
  for (const suit of SUITS) {
    for (const rank of RANKS) {
      lines.push(`  '${rank}_${suit}': require('../../../assets/cards/${deckId}/${rank}_${suit}.png'),`);
    }
  }
  lines.push(`  back: require('../../../assets/cards/${deckId}/back.png'),`);
  lines.push(`};`);
  lines.push(`export default images;`);

  fs.writeFileSync(path.join(IMAGES_DIR, `${deckId}.ts`), lines.join('\n') + '\n');
  console.log(`✓ src/decks/images/${deckId}.ts`);
}

console.log('\nDone. Drop real PNG files into assets/cards/<deckId>/ to replace placeholders.');
